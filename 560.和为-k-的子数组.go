// @lc code=start
func subarraySum(nums []int, k int) int {
	count, pre := 0, 0
	m := map[int]int{}
	//base case
	m[0] = 1
	for i := 0; i < len(nums); i++ {
		pre += nums[i]
		if _, ok := m[pre-k]; ok {
			count += m[pre-k]
		}
		m[pre] += 1
	}
	return count
}

